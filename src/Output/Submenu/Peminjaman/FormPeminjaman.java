    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (this.buku == null) {
            this.peringatan("Pilih buku yang ingin dipinjam terlebih dahulu!");
        } else if (this.mahasiswa == null) {
            this.peringatan("Pilih mahasiswa yang ingin meminjam buku terlebih dahulu!");
        } else {
            try {
                SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
                Date tanggalPinjam = dateFormat.parse(inputTanggalPeminjam.getText().trim());
                Date tanggalRencanaKembali = dateFormat.parse(inputTanggalRencanaKembali.getText().trim());
                Integer denda = Integer.parseInt(inputDenda.getText().trim());
                Borrows newPeminjaman = new Borrows();
                newPeminjaman.setTanggalPinjam(tanggalPinjam);
                newPeminjaman.setRencanaKembali(tanggalRencanaKembali);
                newPeminjaman.setBanyaknya(1);
                newPeminjaman.setDenda(denda);
                newPeminjaman.setBooksId(buku);
                newPeminjaman.setTesisId(null);
                newPeminjaman.setStudentId(mahasiswa);

                TypedQuery<Users> getUserById = entityManager.createNamedQuery("Users.findByUserId", Users.class);
                getUserById.setParameter("userId", this.userId);
                newPeminjaman.setUserId(getUserById.getSingleResult());

                Date date = new Date();
                newPeminjaman.setCreatedAt(date);
                newPeminjaman.setUpdatedAt(date);

                entityManager.getTransaction().begin();
                this.buku.setBanyaknya(this.buku.getBanyaknya() - 1);
                this.buku.setUpdatedAt(date);
                entityManager.merge(buku);
                entityManager.persist(newPeminjaman);
                entityManager.getTransaction().commit();

                ((Peminjaman) obj).loadTable();
                this.peringatan("Berhasil menambah data peminjaman!");
                this.dispose();

            } catch (NumberFormatException | ParseException e) {
                System.out.println(e.getMessage());
                entityManager.getTransaction().rollback();
                this.peringatan("Gagal menambahkan data peminjaman!");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    public Date stringToDate(String str) {
        String[] explodeStr = str.split("-");
        return new Date(Integer.parseInt(explodeStr[2]), Integer.parseInt(explodeStr[1]), Integer.parseInt(explodeStr[0]));
    }

