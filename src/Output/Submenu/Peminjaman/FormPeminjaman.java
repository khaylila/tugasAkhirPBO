    public void setBookId(int bookId) {
        TypedQuery<Books> getBookById = entityManager.createNamedQuery("Books.findByBookId", Books.class);
        getBookById.setParameter("bookId", bookId);
        this.buku = getBookById.getSingleResult();
        inputBuku.setText(this.buku.getJudul());
        inputBanyaknya.setText("1");
    }

    public void setMahasiswaId(int mahasiswaId) {
        TypedQuery<Students> getBookById = entityManager.createNamedQuery("Students.findByStudentId", Students.class);
        getBookById.setParameter("studentId", mahasiswaId);
        this.mahasiswa = getBookById.getSingleResult();
        inputPeminjam.setText(this.mahasiswa.getFullname());
    }

