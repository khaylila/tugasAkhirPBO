    public ViewMahasiswa(int userId, int studentId) {
        this.userId = userId;

        initComponents();

        TypedQuery<Students> getStudentById = entityManager.createNamedQuery("Students.findByStudentId", Students.class);
        getStudentById.setParameter("studentId", studentId);
        Students result = getStudentById.getSingleResult();
        try {
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
            labelStudentId.setText("ID : " + String.format("%010d", result.getStudentId()));
            inputNIM.setText(result.getNim());
            inputNamaMahasiswa.setText(result.getFullname());
            inputAngkatan.setText(String.valueOf(result.getAngkatan()));
            inputAlamat.setText(result.getAlamat());
            inputTelepon.setText(result.getTelepon());
            labelCreatedAt.setText("Tgl. Ditambahkan: " + dateFormat.format(result.getCreatedAt()));
            labelUpdatedAt.setText("Terakhir Diubah: " + dateFormat.format(result.getUpdatedAt()));

            SimpleDateFormat simpleDate = new SimpleDateFormat("dd/MM/yyyy");
//          begin daftar peminjam
            DefaultTableModel model = (DefaultTableModel) tabelPeminjaman.getModel();
            model.setRowCount(0);
            for (Borrows pinjam : result.getBorrowsList()) {
                Object[] baris = new Object[4];
                if (pinjam.getBooksId() == null) {
                    baris[0] = pinjam.getTesisId().getJudul();
                    baris[1] = "Skripsi";
                } else {
                    baris[0] = pinjam.getBooksId().getJudul();
                    baris[1] = "Buku";
                }
                baris[2] = simpleDate.format(pinjam.getTanggalPinjam());
                baris[3] = (pinjam.getTanggalKembali() == null ? "Blm kembali" : "Dikembalikan");
                model.addRow(baris);
            }
//          end daftar peminjam

//          begin sumbang buku
            DefaultTableModel tbRiwayat = (DefaultTableModel) tabelRiwayatBuku.getModel();
            tbRiwayat.setRowCount(0);
            for (Books buku : result.getBooksList()) {
                Object[] baris = new Object[3];
                baris[0] = buku.getJudul();
                baris[1] = "Buku";
                baris[2] = simpleDate.format(buku.getCreatedAt());
                tbRiwayat.addRow(baris);
            }
//          end sumbang buku

//          begin skripsi
            for (Thesis tesis : result.getThesisList()) {
                Object[] baris = new Object[3];
                baris[0] = tesis.getJudul();
                baris[1] = "Skripsi";
                baris[2] = simpleDate.format(tesis.getCreatedAt());
                tbRiwayat.addRow(baris);
            }
//          end skripsi

            this.student = result;
        } catch (NoResultException e) {
            this.peringatan("Data tidak ditemukan!");
            this.dispose();
        }

    }

