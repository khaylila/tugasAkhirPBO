    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int result = this.dialog(new String[]{"Hapus Mahasiswa", "Yakin ingin menghapus data mahasiswa?"});
        if (result == JOptionPane.YES_OPTION) {
            try {
                entityManager.getTransaction().begin();
                entityManager.remove(this.student);
                entityManager.getTransaction().commit();
                this.peringatan("Berhasil menghapus data mahasiswa!");
                this.dispose();
            } catch (Exception e) {
                entityManager.getTransaction().rollback();
                this.peringatan("Gagal menghapus mahasiswa!");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

