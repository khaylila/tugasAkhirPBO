    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String nama = inputNama.getText();
        if (nama.isEmpty()) {
            this.peringatan("Kategori tidak boleh kosong!");
        } else {
            try {
                Categories oldCategory = this.kategori;
                oldCategory.setNama(inputNama.getText());
                entityManager.getTransaction().begin();
                entityManager.merge(oldCategory);
                entityManager.getTransaction().commit();
                this.peringatan("Berhasil merubah kategori!");
                if (obj instanceof FormKategori) {
                    ((FormKategori) obj).loadTable();
                    this.dispose();
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
                entityManager.getTransaction().rollback();
                this.peringatan("Gagal merubah kategori!");
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

