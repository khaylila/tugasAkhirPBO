    public FormEditKategori(Object obj, int kategoriId) {
        try {
            TypedQuery<Categories> getCategoryById = entityManager.createNamedQuery("Categories.findByKategoriId", Categories.class);
            getCategoryById.setParameter("kategoriId", kategoriId);
            this.kategori = getCategoryById.getSingleResult();
            this.obj = obj;
            initComponents();

            inputNama.setText(this.kategori.getNama());
        } catch (NullPointerException e) {
            System.out.println(this.kategori.getKategoriId());
            this.peringatan("Pilih kategori terlebih dahulu!");
        }
    }

