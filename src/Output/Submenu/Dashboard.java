    public Dashboard(int userId, String panel) {
        this.userId = userId;
        initComponents();
        if (panel.equalsIgnoreCase("Books")) {
            panelMain.removeAll();
            panelMain.add(new BookNew(this, userId));
            panelMain.repaint();
            panelMain.revalidate();
        } else if (panel.equalsIgnoreCase("Thesis")) {
            panelMain.removeAll();
            panelMain.add(new TesisView(this, userId));
            panelMain.repaint();
            panelMain.revalidate();
        } else if (panel.equalsIgnoreCase("Students")) {
            panelMain.removeAll();
            panelMain.add(new Mahasiswa(this, userId));
            panelMain.repaint();
            panelMain.revalidate();
        }
    }

