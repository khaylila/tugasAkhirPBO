    public void loadTabel() {
        String search = inputSearch.getText().trim();
        if (search.equalsIgnoreCase("search")) {
            search = "";
        }
        int filter = searchBy.getSelectedIndex();
        String query = "Thesis.findByLikeJudul";
        if (filter == 1) {
            query = "Thesis.findByLikeStudent";
        }

        TypedQuery<Thesis> queryListTesis = null;
        queryListTesis = entityManager.createNamedQuery(query, Thesis.class);
        queryListTesis.setParameter("parameter", "%" + search + "%");
        List<Thesis> listThesis = queryListTesis.getResultList();

        this.listSkripsi.clear();
        DefaultTableModel model = (DefaultTableModel) tabelSkripsi.getModel();
        model.setRowCount(0);
        int i = 1;
        for (Thesis data : listThesis) {
            Object[] baris = new Object[4];
            baris[0] = i++;
            this.listSkripsi.add(data);
            baris[1] = data.getJudul();
            baris[2] = data.getStudentId().getFullname();
            baris[3] = data.getTahunTerbit();
            model.addRow(baris);
        }
    }

