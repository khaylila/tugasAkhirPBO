    private void tabelSkripsiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelSkripsiMouseClicked
        // TODO add your handling code here:
        JTable target = (JTable) evt.getSource();
        int row = target.getSelectedRow();
        int indexId = Integer.parseInt(target.getModel().getValueAt(row, 0).toString());
        this.selectedSkripsi = listSkripsi.get(indexId - 1);
        this.allowBook = (this.selectedSkripsi.getBanyaknya() >= 1 ? true : false);
    }//GEN-LAST:event_tabelSkripsiMouseClicked

    private void btnPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihActionPerformed
        // TODO add your handling code here:
        if (this.selectedSkripsi == null) {
            this.peringatan("Pilih buku terlebih dahulu!");
        } else if (!this.allowBook) {
            this.peringatan("Stok buku sudah habis!");
        } else {
            if (this.obj instanceof FormPeminjamanSkripsi) {
                ((FormPeminjamanSkripsi) this.obj).setTesisId(this.selectedSkripsi.getTesisId());
                this.dispose();
            } else {
                System.out.println("Ini bukan instansi FormPeminjamanSkripsi!");
            }
        }
    }//GEN-LAST:event_btnPilihActionPerformed

